package edu.illinois.library.metaslurper.entity;

public enum Variant {

    /**
     * Book.
     */
    BOOK,

    /**
     * Collection of items (or anything).
     */
    COLLECTION,

    /**
     * Grouping of data files, such as a research data set.
     */
    DATA_SET,

    /**
     * Person, place, thing, idea, or any other kind of noun.
     */
    ENTITY,

    /**
     * File on disk.
     */
    FILE,

    /**
     * Digital object or item.
     */
    ITEM,

    /**
     * Newspaper page.
     */
    NEWSPAPER_PAGE,

    /**
     * Article or other publication.
     */
    PAPER,

    /**
     * Something else.
     */
    UNKNOWN

}
